    private void btnremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveActionPerformed
       
        try{

            String userid=jcUserid.getSelectedItem().toString();
             
            boolean result = ReceptionistDAO.deleteEmloyee(userid);
            if(result){
                JOptionPane.showMessageDialog(null, "Recored removed sucessfully","Sucess",JOptionPane.INFORMATION_MESSAGE);
                jcUserid.removeItemAt(jcUserid.getSelectedIndex());
                if(jcUserid.getItemCount()==0)
                    btnremove.setEnabled(false);
                return;
            }
            
            JOptionPane.showMessageDialog(null, "Record not removed","Error",JOptionPane.ERROR_MESSAGE);

        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Database Error!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnremoveActionPerformed

