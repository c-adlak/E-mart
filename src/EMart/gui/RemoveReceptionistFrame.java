private void getReceptionist() {
       try{
           jcUserid.removeAllItems();
           List<String> alluserId = ReceptionistDAO.getAllReceptionistuserId();
           if(alluserId.isEmpty()){
              JOptionPane.showMessageDialog(null, "No receptionist present!");
        return; 
           }
           btnremove.setEnabled(true);
           for(String id:alluserId){
               jcUserid.addItem(id);
           }
       }
       catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Database Error!","Error",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
       }
    }
   
    
